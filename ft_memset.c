#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	unsigned char	*p;

	p = (unsigned char *)s;
	while (len > 0)
	{
		*p++ = (unsigned char) c;
		len--;
	}
	return (s);
}
